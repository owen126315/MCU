boolean SAMConfig() 
{
	pn532_packetbuffer[0] = PN532_COMMAND_SAMCONFIGURATION;
  pn532_packetbuffer[1] = 0x01; // normal mode;
  pn532_packetbuffer[2] = 0x14; // timeout 50ms * 20 = 1 second
  pn532_packetbuffer[3] = 0x01; // use IRQ pin!
	
	if(writeCommand(pn532_packetbuffer, 4))
		return false;
	
}

int8_t writeCommand(const uint8_t *header, uint8_t hlen)
{
	command = header[0];
	uint8_t* dataOut;
	int total_length = 8;
	dataOut[0] = PN532_PREAMBLE;
	dataOut[1] = PN532_STARTCODE1;
	dataOut[2] = PN532_STARTCODE2;
	dataOut[3] = hlen + 1;
	dataOut[4] = ~hlen + 1;
	dataOut[5] = PN532_HOSTTOPN532;
	uint8_t sum = PN532_HOSTTOPN532;
	for(int i = 0; i<hlen; i++,total_length++)
	{
		dataOut[6+i] = header[i];
		sum+=header[i];
	}
	uint8_t checksum = ~sum + 1;
	dataOut[6+hlen-1] = checksum;
	dataOut[6+hlen] = PN532_POSTAMBLE;
	
	HAL_I2C_Master_Transmit(&hi2c1, PN532_I2C_ADDRESS, dataOut, total_length, 1000);

	return readAckFrame();
}

int8_t readAckFrame()
{
	const uint8_t PN532_ACK[] = {0, 0, 0xFF, 0, 0xFF, 0};
  uint8_t ackBuf[sizeof(PN532_ACK)+1];
	HAL_I2C_Master_Receive(&hi2c1, PN532_I2C_ADDRESS, ackBuf, sizeof(PN532_ACK) + 1, 10);
	for(int i = 0; i<sizeof(PN532_ACK); i++)
	{
		if(ackBuf[1+i] != PN532_ACK[i])
			return PN532_INVALID_ACK;
	}
	
	return 0;
}
